    private void btnSupSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSupSaveActionPerformed
        if (!modelSuppliers.validDate(txtSupCode.getText().trim(), txtSupName.getText().trim(), txtSupAddress.getText().trim())) {
            return;

        }
        if (addNewSup) {
            try {
                if (!modelSuppliers.validDateCodeSupplier(txtSupCode.getText().trim())) {
                    JOptionPane.showMessageDialog(this, "DUPLICATE SUPCODE AT " + txtSupCode.getText().trim());
                    return;
                }
                Suppliers sup = new Suppliers(txtSupCode.getText().trim(), txtSupName.getText().trim(), txtSupAddress.getText().trim(), chbCollaboration.isSelected());
                if (modelSuppliers.Insert(sup) == SUCCESS) {
                    modelSuppliers.getModelSup().getList().add(sup);
                    modelSuppliers.getListSuppliersComboBox().add(sup.getSupCode() + " - " + sup.getSupName());
                }
                addNewSup = false;
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
            try {
                int selectedRow = tblSupplier.getSelectedRow();
                if (selectedRow < 0) {
                    JOptionPane.showMessageDialog(this, "Choose Row To Edit!!");
                    return;
                }
                Suppliers supNew = new Suppliers(txtSupCode.getText().trim(), txtSupName.getText().trim(), txtSupAddress.getText().trim(), chbCollaboration.isSelected());
                Suppliers sup01d = modelSuppliers.getModelSup().getList().get(selectedRow);
                if (modelSuppliers.Save(supNew) == SUCCESS) {
                    modelSuppliers.getListSuppliersComboBox().remove(sup01d.getSupCode() + " - " + sup01d.getSupName());
                    sup01d.setSupName(supNew.getSupName());
                    sup01d.setAddress(supNew.getAddress());
                    sup01d.setColloborating(supNew.isColloborating());
                    modelSuppliers.getListSuppliersComboBox().add(supNew.getSupCode() + " - " + supNew.getSupCode());
                }

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        modelItem.getModelItem().loadSuppliersComboBox();
        cbSuppliers.setModel(new DefaultComboBoxModel<>(modelSuppliers.getListSuppliersComboBox()));
        tblSupplier.updateUI();

    }//GEN-LAST:event_btnSupSaveActionPerformed

    private void btnSupDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSupDeleteActionPerformed
        try {
            int selectedRow = tblSupplier.getSelectedRow();
            if (selectedRow < 0 || deleteSup == false) {
                JOptionPane.showMessageDialog(this, "Choose the Row to delelte");
                return;
            }
            int r = JOptionPane.showConfirmDialog(this, "Do you want to del this Suppliers?");
            if (r == JOptionPane.YES_OPTION) {
                Suppliers sup = modelSuppliers.getModelSup().getList().get(selectedRow);
                if (!modelItem.checkconstraintSupCode(sup.getSupCode())) {
                    JOptionPane.showMessageDialog(this, "Still items is manager by this Suppliers");
                    return;
                }
                if (modelSuppliers.Delete(sup) == SUCCESS) {
                    deleteSup = false;
                    modelSuppliers.getModelSup().getList().remove(sup);
                    modelSuppliers.getListSuppliersComboBox().remove(sup.getSupCode() + "-" + sup.getSupName());
                    JOptionPane.showMessageDialog(this, "Deleted!");
                    cbSuppliers.setModel(new DefaultComboBoxModel<>(modelSuppliers.getListSuppliersComboBox()));
                    clearInformationSupplierOnTable();
                    modelItem.getModelItem().loadSuppliersComboBox();
                    tblSupplier.updateUI();

                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSupDeleteActionPerformed

