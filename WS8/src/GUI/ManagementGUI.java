    private void btnItemSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnItemSaveActionPerformed
        if (!modelItem.validDate(txtItemCode.getText().trim(), txtItemName.getText().trim(), txtUnit.getText().trim(), txtPrice.getText().trim(), cbSuppliers.getSelectedItem())) {
            return;
        }
        if (addNewItem) {
            try {
                if (!modelItem.validDateCodeItem(txtItemCode.getText().trim())) {
                    JOptionPane.showMessageDialog(this, "DUPLICATED ITEMCODE at " + txtItemCode.getText().trim());
                    return;
                }
                Item it = new Item(txtItemCode.getText().toUpperCase().trim(), txtItemName.getText().trim(), txtUnit.getText().trim(),
                        Float.parseFloat(txtPrice.getText().trim()), chbSupplying.isSelected(), modelItem.loadSupCodeIntoItems(cbSuppliers.getSelectedItem().toString()));
                if (modelItem.Insert(it) == SUCCESS) {
                    modelItem.getModelItem().getList().add(it);
                }
                addNewItem = false;
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
            try {
                int selectedRow = tblItems.getSelectedRow();
                if (selectedRow < 0) {
                    JOptionPane.showMessageDialog(this, "Please Choose Row to Edit");
                }
                Item it0ld = modelItem.getModelItem().getList().get(selectedRow);
                Item itNew = new Item(txtItemCode.getText().toUpperCase().trim(), txtItemName.getText().trim(), txtUnit.getText().trim(),
                        Float.parseFloat(txtPrice.getText().trim()), chbSupplying.isSelected(), modelItem.loadSupCodeIntoItems(cbSuppliers.getSelectedItem().toString()));
                if (modelItem.Save(itNew) == SUCCESS) {
                    it0ld.setItemName(itNew.getItemName());
                    it0ld.setPrice(itNew.getPrice());
                    it0ld.setUnit(itNew.getUnit());
                    it0ld.setSupCode(itNew.getSupCode());
                    it0ld.setSupplying(itNew.isSupplying());
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        tblItems.updateUI();
    }//GEN-LAST:event_btnItemSaveActionPerformed

